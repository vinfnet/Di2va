// ─── Fetch & Parse Original FIT File from Strava ────────────────────────────

app.get('/api/activity/:id/fit-gear-data', async (req, res) => {
  const token = await ensureValidToken(req);
  if (!token) return res.status(401).json({ error: 'Not authenticated' });

  try {
    // Strava exposes the original uploaded file at this endpoint.
    // NOTE: This is not an official public API — it may return 403 or redirect
    // to a web login page. When it fails we fall back to gear estimation.
    console.log(`Fetching original FIT for activity ${req.params.id}...`);
    const response = await axios.get(
      `${STRAVA_API_BASE}/activities/${req.params.id}/export_original`,
      {
        headers: { Authorization: `Bearer ${token}` },
        responseType: 'arraybuffer',
        timeout: 30000,
        maxRedirects: 5
      }
    );

    const contentType = response.headers['content-type'] || '';
    const buffer = Buffer.from(response.data);
    console.log(`  Got ${buffer.length} bytes, content-type: ${contentType}`);

    // Check if we actually got a FIT file (starts with header size byte + ".FIT")
    const isFit = buffer.length > 12 &&
                  buffer.toString('ascii', 8, 12) === '.FIT';

    if (!isFit && !contentType.includes('octet-stream') && !contentType.includes('fit')) {
      return res.json({
        has_gear_data: false,
        source: 'strava_export',
        error: 'Original file is not a FIT file (may be GPX or TCX)'
      });
    }

    // Parse the FIT file
    const fitParser = new FitParser({
      force: true,
      speedUnit: 'km/h',
      lengthUnit: 'km',
      temperatureUnit: 'celsius',
      elapsedRecordField: true,
      mode: 'both'
    });

    fitParser.parse(buffer, (error, data) => {
      if (error) {
        console.error('FIT parse error (from Strava):', error);
        return res.json({
          has_gear_data: false,
          source: 'strava_export',
          error: 'Failed to parse FIT file'
        });
      }

      const result = extractDi2Data(data);
      result.source = 'strava_export';
      res.json(result);
    });

  } catch (err) {
    const status = err.response?.status;
    // 404 = no original file, 403 = not authorized for this activity
    if (status === 404 || status === 403) {
      return res.json({
        has_gear_data: false,
        source: 'strava_export',
        error: status === 404
          ? 'No original file available for this activity'
          : 'Not authorized to access original file'
      });
    }
    console.error('FIT fetch error:', status, err.message);
    console.log('  → This is expected — export_original is not an official Strava API endpoint.');
    console.log('  → For actual Di2 data, upload the .FIT file from your bike computer.');
    res.json({
      has_gear_data: false,
      source: 'strava_export',
      error: 'Failed to fetch original file from Strava'
    });
  }
});

