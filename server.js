function isStravaConfigured() {
  return process.env.STRAVA_CLIENT_ID &&
         process.env.STRAVA_CLIENT_ID.trim() !== '' &&
         process.env.STRAVA_CLIENT_SECRET &&
         process.env.STRAVA_CLIENT_SECRET.trim() !== '';
}

// Serve setup page
