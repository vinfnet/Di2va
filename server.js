app.post('/api/parse-fit', upload.single('fitfile'), (req, res) => {
  if (!req.file) {
    return res.status(400).json({ error: 'No file uploaded' });
  }

  const filePath = req.file.path;

  try {
    const fileBuffer = fs.readFileSync(filePath);
    const fitParser = new FitParser({
      force: true,
      speedUnit: 'km/h',
      lengthUnit: 'km',
      temperatureUnit: 'celsius',
      elapsedRecordField: true,
      mode: 'both'
    });

    fitParser.parse(fileBuffer, (error, data) => {
      // Clean up uploaded file
      fs.unlinkSync(filePath);

      if (error) {
        console.error('FIT parse error:', error);
        return res.status(400).json({ error: 'Failed to parse FIT file' });
      }

      // Extract gear shift data from events and records
      const result = extractDi2Data(data);
      res.json(result);
    });
  } catch (err) {
    // Clean up on error
    if (fs.existsSync(filePath)) fs.unlinkSync(filePath);
    console.error('FIT processing error:', err);
    res.status(500).json({ error: 'Failed to process FIT file' });
  }
});

/**
 * Decode gear data packed in a FIT event's uint32 `data` field.
 * Layout: byte0 = rear_gear_num, byte1 = rear_gear_teeth,
 *         byte2 = front_gear_num, byte3 = front_gear_teeth
 */
function decodeGearData(data) {
  if (typeof data !== 'number' || data === 0) return null;
  return {
    rear_gear_num:    data & 0xFF,
    rear_gear_teeth:  (data >> 8) & 0xFF,
    front_gear_num:   (data >> 16) & 0xFF,
    front_gear_teeth: (data >> 24) & 0xFF
  };
}

/**
 * Extract Di2 gear shift data from parsed FIT file.
 * Handles rear_gear_change / front_gear_change events (Garmin)
 * and the older gear_change event type.
 */
function extractDi2Data(fitData) {
  const gearChanges = [];
  const records = [];

  // Extract gear change events — match all known event names
  if (fitData.events) {
    fitData.events.forEach(event => {
      const isGearEvent =
        event.event === 'rear_gear_change' ||
        event.event === 'front_gear_change' ||
        event.event === 'gear_change' ||
        event.event_type === 'gear_change';

      if (!isGearEvent) return;

      // Try named fields first (some parsers expand them),
      // then decode from the packed uint32 data field
      let rearNum   = event.rear_gear_num;
      let rearTeeth = event.rear_gear_teeth;
      let frontNum  = event.front_gear_num;
      let frontTeeth = event.front_gear_teeth;

      if (rearTeeth === undefined && event.data !== undefined) {
        const decoded = decodeGearData(event.data);
        if (decoded) {
          rearNum   = decoded.rear_gear_num;
          rearTeeth = decoded.rear_gear_teeth;
          frontNum  = decoded.front_gear_num;
          frontTeeth = decoded.front_gear_teeth;
        }
      }

      gearChanges.push({
        timestamp: event.timestamp,
        event_type: event.event,          // 'rear_gear_change' or 'front_gear_change'
        rear_gear_num:    rearNum   || null,
        rear_gear_teeth:  rearTeeth || null,
        front_gear_num:   frontNum  || null,
        front_gear_teeth: frontTeeth || null,
        gear_ratio: (frontTeeth && rearTeeth) ? +(frontTeeth / rearTeeth).toFixed(2) : null
      });
    });
  }

  // Sort gear changes by timestamp (they should already be, but be safe)
  gearChanges.sort((a, b) => new Date(a.timestamp) - new Date(b.timestamp));

  // Extract per-record data (records have timestamps, position, gear info etc.)
  if (fitData.records) {
    fitData.records.forEach(record => {
      const rec = {
        timestamp: record.timestamp,
        elapsed_time: record.elapsed_time,
        distance: record.distance,
        position_lat: record.position_lat,
        position_long: record.position_long,
        altitude: record.altitude || record.enhanced_altitude,
        speed: record.speed || record.enhanced_speed,
        cadence: record.cadence,
        power: record.power,
        heart_rate: record.heart_rate,
        temperature: record.temperature
      };

      // Di2 specific fields that may appear directly in records
      if (record.rear_gear_num !== undefined) rec.rear_gear_num = record.rear_gear_num;
      if (record.rear_gear_teeth !== undefined) rec.rear_gear_teeth = record.rear_gear_teeth;
      if (record.front_gear_num !== undefined) rec.front_gear_num = record.front_gear_num;
      if (record.front_gear_teeth !== undefined) rec.front_gear_teeth = record.front_gear_teeth;
      if (record.left_right_balance !== undefined) rec.left_right_balance = record.left_right_balance;

      records.push(rec);
    });
  }

  // Merge gear change events into records by timestamp
  if (gearChanges.length > 0 && records.length > 0) {
    let gearIdx = 0;
    let currentGear = {
      front_gear_num:   gearChanges[0].front_gear_num,
      front_gear_teeth: gearChanges[0].front_gear_teeth,
      rear_gear_num:    gearChanges[0].rear_gear_num,
      rear_gear_teeth:  gearChanges[0].rear_gear_teeth
    };

    records.forEach(record => {
      // Advance gear index to match timestamp
      while (gearIdx < gearChanges.length &&
             new Date(gearChanges[gearIdx].timestamp) <= new Date(record.timestamp)) {
        const gc = gearChanges[gearIdx];
        // Each event carries full state (front + rear), so update all fields
        if (gc.front_gear_num)   currentGear.front_gear_num   = gc.front_gear_num;
        if (gc.front_gear_teeth) currentGear.front_gear_teeth = gc.front_gear_teeth;
        if (gc.rear_gear_num)    currentGear.rear_gear_num    = gc.rear_gear_num;
        if (gc.rear_gear_teeth)  currentGear.rear_gear_teeth  = gc.rear_gear_teeth;
        gearIdx++;
      }

      // Apply current gear to record if not already set
      if (!record.rear_gear_num)    record.rear_gear_num    = currentGear.rear_gear_num;
      if (!record.rear_gear_teeth)  record.rear_gear_teeth  = currentGear.rear_gear_teeth;
      if (!record.front_gear_num)   record.front_gear_num   = currentGear.front_gear_num;
      if (!record.front_gear_teeth) record.front_gear_teeth = currentGear.front_gear_teeth;

      // Calculate gear ratio
      if (record.front_gear_teeth && record.rear_gear_teeth) {
        record.gear_ratio = +(record.front_gear_teeth / record.rear_gear_teeth).toFixed(2);
      }
    });
  }

  // Extract device info — match Shimano by string name or known manufacturer IDs
  // (manufacturer 41 = Shimano in some FIT profiles, or appears as numeric when
  //  the parser doesn't have the enum mapping)
  const SHIMANO_MFR_IDS = [41, 'shimano'];
  const deviceInfo = fitData.device_infos || [];
  const di2Devices = deviceInfo.filter(d =>
    SHIMANO_MFR_IDS.includes(d.manufacturer) ||
    (d.product_name && d.product_name.toLowerCase().includes('di2')) ||
    (d.product_name && d.product_name.toLowerCase().includes('shimano'))
  );

  const hasGearData = gearChanges.length > 0 ||
                      records.some(r => r.rear_gear_num !== undefined);

  console.log(`  Di2 extraction: ${gearChanges.length} gear changes, ` +
              `${records.length} records, has_gear_data=${hasGearData}`);

  return {
    gear_changes: gearChanges,
    records: records,
    di2_devices: di2Devices,
    session: fitData.sessions?.[0] || null,
    has_gear_data: hasGearData
  };
}

// ─── Gear Estimation from Cadence/Speed ─────────────────────────────────────

app.post('/api/estimate-gears', express.json(), (req, res) => {
  const { cadence, velocity_smooth, distance, latlng, altitude } = req.body;

  if (!cadence || !velocity_smooth) {
    return res.status(400).json({ error: 'Cadence and speed data required' });
  }

  // Common road bike gear configurations
  const CHAINRINGS = [34, 50]; // Compact: 34/50, Standard: 39/53
  const CASSETTE = [11, 12, 13, 14, 15, 17, 19, 21, 23, 25, 28]; // 11-28 cassette
  const WHEEL_CIRCUMFERENCE = 2.105; // meters (700x25c)

  const gearData = [];

  for (let i = 0; i < cadence.length; i++) {
    const rpm = cadence[i];
    const speed = velocity_smooth[i]; // m/s

    if (rpm <= 0 || speed <= 0.5) {
      gearData.push({
        index: i,
        front: null,
        rear: null,
        gear_ratio: null,
        estimated: true
      });
      continue;
    }

    // Calculate actual gear inches from speed and cadence
    // speed (m/s) = cadence (rpm) / 60 * gear_ratio * wheel_circumference
    const actualRatio = (speed / (rpm / 60)) / WHEEL_CIRCUMFERENCE;

    // Find closest matching gear combination
    let bestMatch = null;
    let bestDiff = Infinity;

    for (const front of CHAINRINGS) {
      for (const rear of CASSETTE) {
        const ratio = front / rear;
        const diff = Math.abs(ratio - actualRatio);
        if (diff < bestDiff) {
          bestDiff = diff;
          bestMatch = { front, rear, ratio };
        }
      }
    }

    gearData.push({
      index: i,
      front: bestMatch?.front || null,
      rear: bestMatch?.rear || null,
      gear_ratio: bestMatch?.ratio?.toFixed(2) || null,
      confidence: bestDiff < 0.15 ? 'high' : bestDiff < 0.3 ? 'medium' : 'low',
      estimated: true
    });
  }

  res.json({ gears: gearData });
});

// ─── FIT Library — Auto-match FIT files to Strava activities ────────────────

/**
 * Scan a directory (recursively) for .FIT files and extract their start time
 * by parsing just the session header. Returns an array of { path, startTime }.
 *
 * Results are cached in memory and refreshed when the folder changes.
 */
let fitLibraryCache = { folder: null, files: [], scannedAt: 0 };

function getFitFolder() {
  const configured = (process.env.FIT_FOLDER || '').trim();
  if (configured) return configured;
  // Default to ~/Downloads so auto-download from Strava just works
  const downloads = path.join(os.homedir(), 'Downloads');
  return fs.existsSync(downloads) ? downloads : '';
}

function setFitFolder(folder) {
  process.env.FIT_FOLDER = folder;
  fitLibraryCache = { folder: null, files: [], scannedAt: 0 }; // invalidate

  // Persist to .env.local
  const envLocalPath = path.join(__dirname, '.env.local');
  let content = '';
  if (fs.existsSync(envLocalPath)) {
    content = fs.readFileSync(envLocalPath, 'utf8');
    // Remove existing FIT_FOLDER line
    content = content.replace(/^FIT_FOLDER=.*\n?/m, '');
  }
  content = content.trimEnd() + `\nFIT_FOLDER=${folder}\n`;
  fs.writeFileSync(envLocalPath, content, { mode: 0o600 });
}

/**
 * Recursively find all .fit/.FIT files in a directory.
 */
function findFitFiles(dir, maxDepth = 3, depth = 0) {
  if (depth > maxDepth) return [];
  const results = [];
  try {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory() && !entry.name.startsWith('.')) {
        results.push(...findFitFiles(fullPath, maxDepth, depth + 1));
      } else if (entry.isFile() && /\.fit$/i.test(entry.name)) {
        results.push(fullPath);
      }
    }
  } catch (err) {
    // Permission denied or missing dir — skip silently
  }
  return results;
}

/**
 * Quick-parse a FIT file to extract just the session start time.
 * Returns a Date or null.
 */
function getFitStartTime(filePath) {
  try {
    const buf = fs.readFileSync(filePath);
    // Quick sanity check for FIT header
    if (buf.length < 14 || buf.toString('ascii', 8, 12) !== '.FIT') return null;

    const parser = new FitParser({ force: true, mode: 'list' });
    let startTime = null;

    // Synchronous: parse returns via callback immediately for file buffer
    parser.parse(buf, (err, data) => {
      if (err) return;
      // Try session start_time first, then first record timestamp, then file_id time_created
      if (data.sessions?.[0]?.start_time) {
        startTime = new Date(data.sessions[0].start_time);
      } else if (data.records?.[0]?.timestamp) {
        startTime = new Date(data.records[0].timestamp);
      } else if (data.file_ids?.[0]?.time_created) {
        startTime = new Date(data.file_ids[0].time_created);
      }
    });

    return startTime;
  } catch {
    return null;
  }
}

/**
 * Scan the configured FIT folder and build an index of files + start times.
 * Caches for 60 seconds to avoid rescanning on every request.
 */
function scanFitLibrary(quiet = false) {
  const folder = getFitFolder();
  if (!folder) return [];

  const now = Date.now();
  // Use short 5-second cache so polling picks up new downloads quickly
  if (fitLibraryCache.folder === folder && (now - fitLibraryCache.scannedAt) < 5000) {
    return fitLibraryCache.files;
  }

  if (!quiet) console.log(`[FIT Library] Scanning ${folder}...`);
  const fitPaths = findFitFiles(folder);
  if (!quiet) console.log(`[FIT Library] Found ${fitPaths.length} .FIT files, indexing start times...`);

  const files = [];
  for (const fp of fitPaths) {
    const startTime = getFitStartTime(fp);
    if (startTime && !isNaN(startTime.getTime())) {
      files.push({ path: fp, startTime });
    }
  }

  files.sort((a, b) => b.startTime - a.startTime); // newest first
  const prevCount = fitLibraryCache.files?.length || 0;
  fitLibraryCache = { folder, files, scannedAt: now };
  if (!quiet || files.length !== prevCount) {
    console.log(`[FIT Library] Indexed ${files.length} files with valid timestamps`);
  }
  return files;
}

/**
 * Find the FIT file whose start time is closest to the given activity start time.
 * Returns the file entry or null if nothing within 5 minutes.
 */
function findMatchingFit(activityStartTime) {
  const files = scanFitLibrary();
  if (files.length === 0) return null;

  const targetMs = new Date(activityStartTime).getTime();
  if (isNaN(targetMs)) return null;

  let best = null;
  let bestDiff = Infinity;

  for (const f of files) {
    const diff = Math.abs(f.startTime.getTime() - targetMs);
    if (diff < bestDiff) {
      bestDiff = diff;
      best = f;
    }
  }

  // Accept matches within 5 minutes (Strava sometimes adjusts start time)
  const MAX_DIFF_MS = 5 * 60 * 1000;
  if (bestDiff <= MAX_DIFF_MS) {
    return best;
  }
  return null;
}

// API: Get FIT library status
app.get('/api/fit-library/status', (req, res) => {
  const folder = getFitFolder();
  if (!folder) {
    return res.json({ configured: false, folder: null, fileCount: 0 });
  }

  const files = scanFitLibrary();
  res.json({
    configured: true,
    folder,
    fileCount: files.length,
    newestFile: files[0]?.startTime || null,
    oldestFile: files[files.length - 1]?.startTime || null
  });
});

// API: Configure FIT library folder
app.post('/api/fit-library/configure', express.json(), (req, res) => {
  const { folder } = req.body;

  if (!folder || typeof folder !== 'string') {
    return res.status(400).json({ error: 'Folder path is required' });
  }

  const resolved = path.resolve(folder.trim());

  if (!fs.existsSync(resolved)) {
    return res.status(400).json({ error: `Folder not found: ${resolved}` });
  }

  if (!fs.statSync(resolved).isDirectory()) {
    return res.status(400).json({ error: `Not a directory: ${resolved}` });
  }

  setFitFolder(resolved);

  // Scan immediately to return count
  const files = scanFitLibrary();

  console.log(`\n✅ FIT Library folder set to: ${resolved} (${files.length} files indexed)`);

  res.json({
    configured: true,
    folder: resolved,
    fileCount: files.length,
    newestFile: files[0]?.startTime || null,
    oldestFile: files[files.length - 1]?.startTime || null
  });
});

// API: Auto-match a FIT file to a Strava activity by start time
app.get('/api/activity/:id/auto-fit', async (req, res) => {
  const folder = getFitFolder();
  if (!folder) {
    return res.json({ has_gear_data: false, source: 'fit_library', error: 'FIT library not configured' });
  }

  // Get activity start time from query param or fetch from Strava
  let startTime = req.query.start_date;

  if (!startTime) {
    // Fetch from Strava
    const token = await ensureValidToken(req);
    if (!token) return res.status(401).json({ error: 'Not authenticated' });

    try {
      const api = stravaApi(token);
      const resp = await api.get(`/activities/${req.params.id}`);
      startTime = resp.data.start_date;
    } catch (err) {
      return res.json({ has_gear_data: false, source: 'fit_library', error: 'Failed to fetch activity' });
    }
  }

  // Force rescan (quiet mode) so newly-downloaded files are picked up
  fitLibraryCache.scannedAt = 0;
  const match = findMatchingFit(startTime);
  if (!match) {
    return res.json({
      has_gear_data: false,
      source: 'fit_library',
      error: 'No matching FIT file found for this activity'
    });
  }

  console.log(`[FIT Library] Matched activity ${req.params.id} → ${path.basename(match.path)}`);

  // Parse the matched FIT file
  try {
    const fileBuffer = fs.readFileSync(match.path);
    const fitParser = new FitParser({
      force: true,
      speedUnit: 'km/h',
      lengthUnit: 'km',
      temperatureUnit: 'celsius',
      elapsedRecordField: true,
      mode: 'both'
    });

    fitParser.parse(fileBuffer, (error, data) => {
      if (error) {
        return res.json({ has_gear_data: false, source: 'fit_library', error: 'Failed to parse FIT file' });
      }

      const result = extractDi2Data(data);
      result.source = 'fit_library';
      result.matched_file = path.basename(match.path);
      res.json(result);
    });
  } catch (err) {
    console.error('[FIT Library] Parse error:', err.message);
    res.json({ has_gear_data: false, source: 'fit_library', error: 'Failed to read FIT file' });
  }
});

// API: Rescan FIT library (force cache invalidation)
app.post('/api/fit-library/rescan', (req, res) => {
  fitLibraryCache = { folder: null, files: [], scannedAt: 0 };
  const files = scanFitLibrary();
  res.json({ fileCount: files.length });
});

