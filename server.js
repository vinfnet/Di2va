// ─── Fetch & Parse Original FIT File from Strava ────────────────────────────

app.get('/api/activity/:id/fit-gear-data', async (req, res) => {
  const token = await ensureValidToken(req);
  if (!token) return res.status(401).json({ error: 'Not authenticated' });

  try {
    // Strava exposes the original uploaded file at this endpoint.
    // NOTE: This is not an official public API — it may return 403 or redirect
    // to a web login page. When it fails we fall back to gear estimation.
    console.log(`Fetching original FIT for activity ${req.params.id}...`);
    const response = await axios.get(
      `${STRAVA_API_BASE}/activities/${req.params.id}/export_original`,
      {
        headers: { Authorization: `Bearer ${token}` },
        responseType: 'arraybuffer',
        timeout: 30000,
        maxRedirects: 5
      }
    );

    const contentType = response.headers['content-type'] || '';
    const buffer = Buffer.from(response.data);
    console.log(`  Got ${buffer.length} bytes, content-type: ${contentType}`);

    // Check if we actually got a FIT file (starts with header size byte + ".FIT")
    const isFit = buffer.length > 12 &&
                  buffer.toString('ascii', 8, 12) === '.FIT';

    if (!isFit && !contentType.includes('octet-stream') && !contentType.includes('fit')) {
      return res.json({
        has_gear_data: false,
        source: 'strava_export',
        error: 'Original file is not a FIT file (may be GPX or TCX)'
      });
    }

    // Parse the FIT file
    const fitParser = new FitParser({
      force: true,
      speedUnit: 'km/h',
      lengthUnit: 'km',
      temperatureUnit: 'celsius',
      elapsedRecordField: true,
      mode: 'both'
    });

    fitParser.parse(buffer, (error, data) => {
      if (error) {
        console.error('FIT parse error (from Strava):', error);
        return res.json({
          has_gear_data: false,
          source: 'strava_export',
          error: 'Failed to parse FIT file'
        });
      }

      const result = extractDi2Data(data);
      result.source = 'strava_export';
      res.json(result);
    });

  } catch (err) {
    const status = err.response?.status;
    // 404 = no original file, 403 = not authorized for this activity
    if (status === 404 || status === 403) {
      return res.json({
        has_gear_data: false,
        source: 'strava_export',
        error: status === 404
          ? 'No original file available for this activity'
          : 'Not authorized to access original file'
      });
    }
    console.error('FIT fetch error:', status, err.message);
    console.log('  → This is expected — export_original is not an official Strava API endpoint.');
    console.log('  → For actual Di2 data, upload the .FIT file from your bike computer.');
    res.json({
      has_gear_data: false,
      source: 'strava_export',
      error: 'Failed to fetch original file from Strava'
    });
  }
});

// ─── FIT File Upload & Parsing ──────────────────────────────────────────────

app.post('/api/parse-fit', upload.single('fitfile'), (req, res) => {
  if (!req.file) {
    return res.status(400).json({ error: 'No file uploaded' });
  }

  const filePath = req.file.path;

  try {
    const fileBuffer = fs.readFileSync(filePath);
    const fitParser = new FitParser({
      force: true,
      speedUnit: 'km/h',
      lengthUnit: 'km',
      temperatureUnit: 'celsius',
      elapsedRecordField: true,
      mode: 'both'
    });

    fitParser.parse(fileBuffer, (error, data) => {
      // Clean up uploaded file
      fs.unlinkSync(filePath);

      if (error) {
        console.error('FIT parse error:', error);
        return res.status(400).json({ error: 'Failed to parse FIT file' });
      }

      // Extract gear shift data from events and records
      const result = extractDi2Data(data);
      res.json(result);
    });
  } catch (err) {
    // Clean up on error
    if (fs.existsSync(filePath)) fs.unlinkSync(filePath);
    console.error('FIT processing error:', err);
    res.status(500).json({ error: 'Failed to process FIT file' });
  }
});

/**
 * Extract Di2 gear shift data from parsed FIT file.
 * Looks for gear_change events and device-specific records.
 */
function extractDi2Data(fitData) {
  const gearChanges = [];
  const records = [];

  // Extract gear change events
  if (fitData.events) {
    fitData.events.forEach(event => {
      if (event.event === 'rider_position_change' ||
          event.event === 'gear_change' ||
          event.event_type === 'gear_change') {
        gearChanges.push({
          timestamp: event.timestamp,
          rear_gear_num: event.rear_gear_num || event.data?.rear_gear_num,
          rear_gear_teeth: event.rear_gear_teeth || event.data?.rear_gear_teeth,
          front_gear_num: event.front_gear_num || event.data?.front_gear_num,
          front_gear_teeth: event.front_gear_teeth || event.data?.front_gear_teeth,
          gear_ratio: event.gear_ratio || null
        });
      }
    });
  }

  // Extract per-record data (records have timestamps, position, gear info etc.)
  if (fitData.records) {
    fitData.records.forEach(record => {
      const rec = {
        timestamp: record.timestamp,
        elapsed_time: record.elapsed_time,
        distance: record.distance,
        position_lat: record.position_lat,
        position_long: record.position_long,
        altitude: record.altitude || record.enhanced_altitude,
        speed: record.speed || record.enhanced_speed,
        cadence: record.cadence,
        power: record.power,
        heart_rate: record.heart_rate,
        temperature: record.temperature
      };

      // Di2 specific fields that may appear in records
      if (record.rear_gear_num !== undefined) rec.rear_gear_num = record.rear_gear_num;
      if (record.rear_gear_teeth !== undefined) rec.rear_gear_teeth = record.rear_gear_teeth;
      if (record.front_gear_num !== undefined) rec.front_gear_num = record.front_gear_num;
      if (record.front_gear_teeth !== undefined) rec.front_gear_teeth = record.front_gear_teeth;
      if (record.left_right_balance !== undefined) rec.left_right_balance = record.left_right_balance;

      records.push(rec);
    });
  }

  // If we have gear change events, merge them into the records
  if (gearChanges.length > 0 && records.length > 0) {
    let gearIdx = 0;
    let currentGear = {
      front_gear_num: gearChanges[0].front_gear_num,
      front_gear_teeth: gearChanges[0].front_gear_teeth,
      rear_gear_num: gearChanges[0].rear_gear_num,
      rear_gear_teeth: gearChanges[0].rear_gear_teeth
    };

    records.forEach(record => {
      // Advance gear index to match timestamp
      while (gearIdx < gearChanges.length &&
             new Date(gearChanges[gearIdx].timestamp) <= new Date(record.timestamp)) {
        currentGear = {
          front_gear_num: gearChanges[gearIdx].front_gear_num || currentGear.front_gear_num,
          front_gear_teeth: gearChanges[gearIdx].front_gear_teeth || currentGear.front_gear_teeth,
          rear_gear_num: gearChanges[gearIdx].rear_gear_num || currentGear.rear_gear_num,
          rear_gear_teeth: gearChanges[gearIdx].rear_gear_teeth || currentGear.rear_gear_teeth
        };
        gearIdx++;
      }

      // Apply current gear to record if not already set
      if (!record.rear_gear_num) record.rear_gear_num = currentGear.rear_gear_num;
      if (!record.rear_gear_teeth) record.rear_gear_teeth = currentGear.rear_gear_teeth;
      if (!record.front_gear_num) record.front_gear_num = currentGear.front_gear_num;
      if (!record.front_gear_teeth) record.front_gear_teeth = currentGear.front_gear_teeth;

      // Calculate gear ratio
      if (record.front_gear_teeth && record.rear_gear_teeth) {
        record.gear_ratio = (record.front_gear_teeth / record.rear_gear_teeth).toFixed(2);
      }
    });
  }

  // Extract device info
  const deviceInfo = fitData.device_infos || [];
  const di2Devices = deviceInfo.filter(d =>
    d.manufacturer === 'shimano' ||
    (d.product_name && d.product_name.toLowerCase().includes('di2'))
  );

  return {
    gear_changes: gearChanges,
    records: records,
    di2_devices: di2Devices,
    session: fitData.sessions?.[0] || null,
    has_gear_data: gearChanges.length > 0 || records.some(r => r.rear_gear_num !== undefined)
  };
}

// ─── Gear Estimation from Cadence/Speed ─────────────────────────────────────

app.post('/api/estimate-gears', express.json(), (req, res) => {
  const { cadence, velocity_smooth, distance, latlng, altitude } = req.body;

  if (!cadence || !velocity_smooth) {
    return res.status(400).json({ error: 'Cadence and speed data required' });
  }

  // Common road bike gear configurations
  const CHAINRINGS = [34, 50]; // Compact: 34/50, Standard: 39/53
  const CASSETTE = [11, 12, 13, 14, 15, 17, 19, 21, 23, 25, 28]; // 11-28 cassette
  const WHEEL_CIRCUMFERENCE = 2.105; // meters (700x25c)

  const gearData = [];

  for (let i = 0; i < cadence.length; i++) {
    const rpm = cadence[i];
    const speed = velocity_smooth[i]; // m/s

    if (rpm <= 0 || speed <= 0.5) {
      gearData.push({
        index: i,
        front: null,
        rear: null,
        gear_ratio: null,
        estimated: true
      });
      continue;
    }

    // Calculate actual gear inches from speed and cadence
    // speed (m/s) = cadence (rpm) / 60 * gear_ratio * wheel_circumference
    const actualRatio = (speed / (rpm / 60)) / WHEEL_CIRCUMFERENCE;

    // Find closest matching gear combination
    let bestMatch = null;
    let bestDiff = Infinity;

    for (const front of CHAINRINGS) {
      for (const rear of CASSETTE) {
        const ratio = front / rear;
        const diff = Math.abs(ratio - actualRatio);
        if (diff < bestDiff) {
          bestDiff = diff;
          bestMatch = { front, rear, ratio };
        }
      }
    }

    gearData.push({
      index: i,
      front: bestMatch?.front || null,
      rear: bestMatch?.rear || null,
      gear_ratio: bestMatch?.ratio?.toFixed(2) || null,
      confidence: bestDiff < 0.15 ? 'high' : bestDiff < 0.3 ? 'medium' : 'low',
      estimated: true
    });
  }

  res.json({ gears: gearData });
});

