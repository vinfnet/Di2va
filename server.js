// ─── Auth middleware ─────────────────────────────────────────────────────────

async function ensureValidToken(req) {
  const strava = req.session.strava;
  if (!strava) return null;

  // Refresh token if expired (or within 5 minutes of expiry)
  if (strava.expires_at < (Date.now() / 1000) + 300) {
    try {
      const response = await axios.post(STRAVA_TOKEN_URL, {
        client_id: process.env.STRAVA_CLIENT_ID,
        client_secret: process.env.STRAVA_CLIENT_SECRET,
        refresh_token: strava.refresh_token,
        grant_type: 'refresh_token'
      });
      strava.access_token = response.data.access_token;
      strava.refresh_token = response.data.refresh_token;
      strava.expires_at = response.data.expires_at;
    } catch (err) {
      console.error('Token refresh error:', err.response?.data || err.message);
      return null;
    }
  }

  return strava.access_token;
}

function stravaApi(token) {
  return axios.create({
    baseURL: STRAVA_API_BASE,
    headers: { Authorization: `Bearer ${token}` }
  });
}

